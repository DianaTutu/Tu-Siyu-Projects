    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) PlantReportTable.getModel();
        model.setRowCount(0);
        for (PlantReportRequest request : account.getWorkQueue().getPlantReportRequestList()) {
            Object[] row = new Object[2];
            row[0] = request;
            row[1] = request.getReport().getStatus();
            model.addRow(row);
        }
    }

