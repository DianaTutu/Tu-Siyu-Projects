    private void processBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = PlantReportTable.getSelectedRow();
        if (selectedRow >= 0) {
            PlantReportRequest a = (PlantReportRequest) PlantReportTable.getValueAt(selectedRow, 0);
            if(a.getReport().getStatus().equalsIgnoreCase("Checked")){
                JOptionPane.showMessageDialog(null, "Already Checked");
            }
            else{
            WritePlantReportJPanel evsjp = new WritePlantReportJPanel(CardPanel, a);
            CardPanel.add("EnterVitalSignsJPanel", evsjp);
            CardLayout layout = (CardLayout) CardPanel.getLayout();
            layout.next(CardPanel);

        } }else {
            JOptionPane.showMessageDialog(null, "Please select a row!");
        }
    }//GEN-LAST:event_processBtnActionPerformed

