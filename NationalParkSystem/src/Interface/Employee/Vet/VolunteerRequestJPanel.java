    private void approveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_approveBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = vrequest.getSelectedRow();
        if (selectedRow >= 0){ 
             VolunteerWorkRequest request = (VolunteerWorkRequest) vrequest.getValueAt(selectedRow, 0);
             
             int number = 0;
             number = request.getProject().getAvailableNum();
             if(number>0){
             request.setStatus("Approved");
             number--;
             request.getProject().setAvailableNum(number);
             populateVolunteerRequest();}
             else{
                  JOptionPane.showMessageDialog(null,"Project is fully resgistered");
             }
        }
        else{
            JOptionPane.showMessageDialog(null,"Select one row", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_approveBtnActionPerformed

