    public void populateVolunteerRequest(){

        DefaultTableModel model = (DefaultTableModel) vrequest.getModel();
        model.setRowCount(0);
        for(VolunteerWorkRequest vr:enterprise.getOrganizationDirectory().getOrganizationList().get(1).getWorkQueue().getVolunteerRequestList()){
            if(vr.getProject().getStatus()=="Finished"){
                vr.setStatus("Declined");
            }
                
            else{
                if(vr.getReceiver()==account)
            {
                Object[] row = new Object[4];
                row[0] = vr;
                row[1] = vr.getSender().getUsername();
                row[2] = vr.getProject().getAvailableNum();
                row[3] = vr.getStatus();
                model.addRow(row);
                }
               
            }}}

