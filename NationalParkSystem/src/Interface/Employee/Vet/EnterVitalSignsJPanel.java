    private void subBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_subBtnActionPerformed
        // TODO add your handling code here:

        if (!minPatternCorrect()) {
            JOptionPane.showMessageDialog(null, "Wrong min_BP pattern!");
            return;
        }
        if (!maxPatternCorrect()) {
            JOptionPane.showMessageDialog(null, "Wrong max_BP pattern!");
            return;
        }
        if (!temPatternCorrect()) {
            JOptionPane.showMessageDialog(null, "Wrongtempreature pattern!");
            return;
        }

        if (!weiPatternCorrect()) {
            JOptionPane.showMessageDialog(null, "Wrong weight pattern!");
            return;
        }
        if (!heiPatternCorrect()) {
            JOptionPane.showMessageDialog(null, "Wrong height pattern!");
            return;
        }
        if (Double.parseDouble(mintxt.getText()) > Double.parseDouble(maxtxt.getText())) {
            JOptionPane.showMessageDialog(null, "Min_BP shuould smaller than Max_BP!");
            return;
        }

        animalReportRequest.getReport().setBody_temp(Double.parseDouble(temtxt.getText()));
        animalReportRequest.getReport().setHeight(Double.parseDouble(heitxt.getText()));
        animalReportRequest.getReport().setMax_bp(Double.parseDouble(maxtxt.getText()));
        animalReportRequest.getReport().setMin_bp(Double.parseDouble(mintxt.getText()));
        
        animalReportRequest.getReport().setWeight(Double.parseDouble(weitxt.getText()));
        animalReportRequest.getReport().setStatus("Checked");

        if (temtxt.getText() != null && heitxt.getText() != null && maxtxt.getText() != null && mintxt.getText() != null && ntxt.getText() != null && weitxt.getText() != null) {
            JOptionPane.showMessageDialog(null, "Successfully Inputted!");
        }
        
        mintxt.setText("");
        maxtxt.setText("");
        temtxt.setText("");
        weitxt.setText("");
        heitxt.setText("");
        subBtn.setEnabled(false);


    }//GEN-LAST:event_subBtnActionPerformed

