    private void doneAnimalBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doneAnimalBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = animalTable.getSelectedRow();
        if (selectedRow >= 0) {
            AnimalReportRequest a = (AnimalReportRequest) animalTable.getValueAt(selectedRow, 0);
            a.setStatus("Handled");
            populateAnimalTable();
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row!");
        }

    }//GEN-LAST:event_doneAnimalBtnActionPerformed

