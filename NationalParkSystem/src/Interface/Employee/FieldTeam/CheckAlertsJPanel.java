    public void populateAnimalTable() {
        DefaultTableModel model = (DefaultTableModel) animalTable.getModel();
        model.setRowCount(0);
        for (AnimalReportRequest request : account.getWorkQueue().getAbnormalAnimalsRequest().getAnimalReportRequests()) {
            Object[] row = new Object[2];
            row[0] = request;
            row[1] = request.getStatus();
            model.addRow(row);
        }

    }

    public void populatePlantTable() {
        DefaultTableModel model = (DefaultTableModel) plantTable.getModel();
        model.setRowCount(0);
        for (PlantReportRequest request : account.getWorkQueue().getAbnormalPlantsRequest().getPlantReportRequests()) {
            Object[] row = new Object[2];
            row[0] = request;
            row[1] = request.getStatus();
            model.addRow(row);
        }

    }

