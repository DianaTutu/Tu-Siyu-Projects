    public boolean passwordCorrect() {
        String PATTERN ="^[A-Za-z0-9_+$]{6,8}$";
               Pattern patt =Pattern.compile(PATTERN);
               Matcher m=patt.matcher(nptxt.getText());
        boolean b =m.matches();
        if(b == false)
        {
            return false;
        } 
        return true;
     }
    
     public void checkButton() {
        if (nptxt.getText().isEmpty()) {
            subBtn.setEnabled(false);
            return;
        }
       
       if(cp.getText().isEmpty()){
           subBtn.setEnabled(false);
       }
        
        if (!nptxt.getText().equals(cp.getText())) {
            subBtn.setEnabled(false);
            return;
        }
        subBtn.setEnabled(true);

    }
    
