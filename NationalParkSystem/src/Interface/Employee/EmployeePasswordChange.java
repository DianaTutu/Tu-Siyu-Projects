    private void nptxtKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nptxtKeyReleased
        // TODO add your handling code here:
        checkButton();
        if (cp.getText().isEmpty()) {
            return;
        }
        if (cp.getText().equals(nptxt.getText())) {
            cp.setBackground(Color.green);
        } else {
            cp.setBackground(Color.red);
        }
    }//GEN-LAST:event_nptxtKeyReleased

