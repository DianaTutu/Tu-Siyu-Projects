    public boolean highPatternCorrect() {
        Pattern p = Pattern.compile("^([1-9][0-9]*)+(.[0-9]{1})?$");
        Matcher m = p.matcher(highTempTXT.getText());
        boolean b = m.matches();
        if (b == false) {
            return false;
        }
        return true;
    }

