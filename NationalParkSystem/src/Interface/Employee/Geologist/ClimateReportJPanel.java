    private void submitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnActionPerformed

        //if (highTempTXT.getText() != null && lowTempTXT.getText() != null && humidityTXT.getText()!=null) {
        if (!lowPatternCorrect()) {
            JOptionPane.showMessageDialog(null, "Wrong Low Temperature pattern!");
            return;
        }

        if (!highPatternCorrect()) {
            JOptionPane.showMessageDialog(null, "Wrong High Temperature pattern!");
            return;
        }
        if (!humPatternCorrect()) {
            JOptionPane.showMessageDialog(null, "Wrong Humidity pattern!");
            return;
        }
        if (Double.parseDouble(highTempTXT.getText()) < Double.parseDouble(lowTempTXT.getText())) {
            JOptionPane.showMessageDialog(null, "Low Temperature shuould be smaller than High Temperature!");
            return;
        }

        ClimateReportRequest cc = new ClimateReportRequest();
        cc.getReport().setMax_t(Double.parseDouble(highTempTXT.getText()));
        cc.getReport().setMin_t(Double.parseDouble(lowTempTXT.getText()));
        cc.getReport().setHumidity(Double.parseDouble(humidityTXT.getText()));
        cc.getReport().setDate(new Date());
        cc.getReport().setStatus("Submitted");
        cc.setType("climate");
        if (account.getWorkQueue().getClimateReportRequests().size() != 0) {
            account.getWorkQueue().getClimateReportRequests().set(0, cc);
            enterprise.getWorkQueue().getClimateReportRequests().set(0, cc);
        } else {
            account.getWorkQueue().getClimateReportRequests().add(cc);
            enterprise.getWorkQueue().getClimateReportRequests().add(cc);
        }
        JOptionPane.showMessageDialog(null, "Successfully submitted!");
        highTempTXT.setText("");
        lowTempTXT.setText("");
        humidityTXT.setText("");
        submitBtn.setEnabled(false);
        
        
        


    }//GEN-LAST:event_submitBtnActionPerformed

