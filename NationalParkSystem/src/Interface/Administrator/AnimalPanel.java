    public void populate() {
        boolean b;
        DefaultTableModel dtm = (DefaultTableModel) individual.getModel();
        dtm.setRowCount(0);
        for (IndividualA an : a.getAnimalIndividualDir().getAnimalList()) {
            b = false;
            for (AnimalReport ar : enterprise.getReportDirectory().getReportDirectory()) {
                if (ar.getName() == an.getName() || ar.getName().equalsIgnoreCase(an.getName())) {
                    Object[] row = new Object[7];
                    row[0] = an;
                    row[1] = ar.getName();
                    row[2] = ar.getMin_bp();
                    row[3] = ar.getMax_bp();
                    row[4] = ar.getBody_temp();
                    row[5] = ar.getWeight();
                    row[6] = ar.getHeight();
                    dtm.addRow(row);
                    b = true;
                    break;
                }
            }
            if (b == false) {
                Object[] row = new Object[7];
                row[0] = an;
                row[1] = an.getName();
                row[2] = an.getMin_bp();
                row[3] = an.getMax_bp();
                row[4] = an.getTemp();
                row[5] = an.getWeight();
                row[6] = an.getHeight();
                dtm.addRow(row);
            }
        }
    }

