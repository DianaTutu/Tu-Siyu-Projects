    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = individual.getSelectedRow();
        if (selectedRow >= 0) {
            IndividualA a = (IndividualA) individual.getValueAt(selectedRow, 0);
            for(AnimalSpecies an: enterprise.getAnimalDirectory().getAnimalList())
                an.getAnimalIndividualDir().getAnimalList().remove(a);
                    
            populate();
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select a row!");
        }
    }//GEN-LAST:event_DeleteActionPerformed


