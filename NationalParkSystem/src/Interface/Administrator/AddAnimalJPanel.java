    public void checkButton() {
        if (ntxt.getText().isEmpty() || mintxt.getText().isEmpty() || maxtxt.getText().isEmpty() || temtxt.getText().isEmpty() || weitxt.getText().isEmpty() || heitxt.getText().isEmpty()) {
            Addbtn.setEnabled(false);
            return;
        } else {
            Addbtn.setEnabled(true);
        }

    }
    
    private void AddbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddbtnActionPerformed
        // TODO add your handling code here:
        if (!namePatternCorrect()) {
            JOptionPane.showMessageDialog(null, "Wrong name pattern!");
            return;
        }

        if (!minPatternCorrect()) {
            JOptionPane.showMessageDialog(null, "Wrong min_BP pattern!");
            return;
        }
        if (!maxPatternCorrect()) {
            JOptionPane.showMessageDialog(null, "Wrong max_BP pattern!");
            return;
        }
        if (!temPatternCorrect()) {
            JOptionPane.showMessageDialog(null, "Wrongtempreature pattern!");
            return;
        }

        if (!weiPatternCorrect()) {
            JOptionPane.showMessageDialog(null, "Wrong weight pattern!");
            return;
        }
        if (!heiPatternCorrect()) {
            JOptionPane.showMessageDialog(null, "Wrong height pattern!");
            return;
        }
        if (Double.parseDouble(mintxt.getText()) > Double.parseDouble(maxtxt.getText())) {
            JOptionPane.showMessageDialog(null, "Min_BP shuould smaller than Max_BP!");
            return;
        }

        double min_bp = Double.parseDouble(mintxt.getText());
        double max_bp = Double.parseDouble(maxtxt.getText());
        double bodytemp = Double.parseDouble(temtxt.getText());
        double weight = Double.parseDouble(weitxt.getText());
        double height = Double.parseDouble(heitxt.getText());
        for (AnimalSpecies an : enterprise.getAnimalDirectory().getAnimalList()) {
            String name = ntxt.getText()+"."+a.getName();
            if (an == a) {
                IndividualA animal = new IndividualA();
                animal.setHeight(height);
                animal.setMax_bp(max_bp);
                animal.setMin_bp(min_bp);
                animal.setTemp(bodytemp);
                animal.setWeight(weight);
                animal.setName(name);
                an.getAnimalIndividualDir().getAnimalList().add(animal);
            }
        }

        JOptionPane.showMessageDialog(null, "Successfully!");
        ntxt.setText("");
        mintxt.setText("");
        maxtxt.setText("");
        temtxt.setText("");
        weitxt.setText("");
        heitxt.setText("");
        Addbtn.setEnabled(false);
    }//GEN-LAST:event_AddbtnActionPerformed

