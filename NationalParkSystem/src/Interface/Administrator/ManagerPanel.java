    private void AssignbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AssignbtnActionPerformed
        // TODO add your handling code here:

        int selectedRow = salary.getSelectedRow();
        if (selectedRow >= 0) {
            UserAccount studentselected = (UserAccount) salary.getValueAt(selectedRow, 0);
            if(studentselected.getPerson().getSalary()!=0.0){
                JOptionPane.showMessageDialog(null, "The salary is assigned");
            }
            else{
            String i = studentselected.getPerson().getName();
            for (UserAccount u : enterprise.getOrganizationDirectory().getOrganizationList().get(1).getUserAccountDirectory().getUserAccountList()) {
                if (u.getPerson().getName().equalsIgnoreCase(i)) {
                    Person p = u.getPerson();
                    String s = JOptionPane.showInputDialog(null, "Salary", "Salary", WIDTH);
                    Double sa = Double.parseDouble(s);
                    if (sa <= salaryTotal) {
                        p.setSalary(p.getSalary() + sa);
                        salaryTotal = salaryTotal - sa;
                        savailable.setText(String.valueOf(salaryTotal));
                        savailable.setEnabled(false);
                        totalMoney = totalMoney - sa;
                        total.setText(String.valueOf(totalMoney));
                        populateEmployee();
//                        populateBudget();
                        populateSalary();
                        JOptionPane.showMessageDialog(null, "The salary is assigned!");

                        return;
                    } else {
                        JOptionPane.showMessageDialog(null, "The salary is not proper!");
                        return;
                    }
                }
            }
        } }else {
            JOptionPane.showMessageDialog(null, "Select one row", "Information", JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_AssignbtnActionPerformed

