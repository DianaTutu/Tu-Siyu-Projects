    public void populateRequest() {
        SimpleDateFormat sdf=new SimpleDateFormat("yyyy/MM/dd");
        DefaultTableModel dtm = (DefaultTableModel) request.getModel();
        dtm.setRowCount(0);
        Enterprise enterprise1 = system.getNetworkList().get(0).getEnterpriseDirectory().getEnterpriseList().get(1);
        for (FundingRequest fr : enterprise1.getWorkQueue().getFundingRequestList()) {
            Object[] row = new Object[5];
            row[0] = fr;
            row[1] = fr.getFunding();
            row[2] = sdf.format(fr.getRequestDate());
            if (String.valueOf(fr.getResolveDate()) == "") {
                row[3] = "Pending";
            }
            row[3] = fr.getResolveDate();
            row[3] = fr.getDescription();
            row[4] = fr.getStatus();
            dtm.addRow(row);
        }

        for (FundingRequest fr : enterprise1.getWorkQueue().getFundingRequestList()) {
            if (fr.getStatus().equalsIgnoreCase("Approved")) {
                funding = funding + fr.getFunding();
            }
        }
        totalMoney = totalMoney + funding;
        df = df + funding;
        total.setText(String.valueOf(totalMoney));
        populateBudget();
    }

