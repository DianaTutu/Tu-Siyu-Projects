    private void populateBudget() {
        DefaultTableModel dtm = (DefaultTableModel) budget.getModel();
        dtm.setRowCount(0);
        Double budget = df;
        Map<String, Double> bud = new HashMap<String, Double>();
        bud.put("National Park Daily Expense", 0.4);
        bud.put("Reseache fee", 0.25);
        bud.put("Employee Salary", 0.25);
        bud.put("Protention & Development", 0.1);
        for (String k : bud.keySet()) {
            if (k.equalsIgnoreCase("Employee Salary")) {
                salaryTotal = bud.get(k) * budget;
                savailable.setText(String.valueOf(salaryTotal));
                savailable.setEnabled(false);
            }
            Object[] row = new Object[3];
            row[0] = k;
            row[1] = String.format("%.2f", bud.get(k) * budget);
            row[2] = bud.get(k) * 100 + "%";
            dtm.addRow(row);
        }

    }

