    private void populateComments() {
        commentsTXT.setEditable(false);
        String commentsString = "";
        if (enterprise.getOrganizationDirectory().getOrganizationList().get(0).getWorkQueue().getCommentsWorkRequests().size() != 0) {
            for (CommentsWorkRequest a : enterprise.getOrganizationDirectory().getOrganizationList().get(0).getWorkQueue().getCommentsWorkRequests()) {
                commentsString += a.toString() + ":\n" + a.getMessage() + "\n\n";
            }
        }
        commentsTXT.setText(commentsString);
    }

