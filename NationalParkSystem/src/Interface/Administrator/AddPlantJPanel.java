    private void AddbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddbtnActionPerformed
        // TODO add your handling code here:
        if (ntxt.getText().isEmpty() ) {
            JOptionPane.showMessageDialog(null, "Please enter all fields!");
            return;
        }
        if (!namePatternCorrect()) {
            JOptionPane.showMessageDialog(null, "Wrong name pattern!");
            return;
        }

        

        for (PlantSpecies an : enterprise.getPlantDirectory().getPlantDir()) {
            String name = ntxt.getText()+"."+an.getName();
            if (an == p) {
                IndividualP plant = new IndividualP();
                plant.setName(name);
                an.getPlantIndividualDirectory().getPlantList().add(plant);
            }
        }

        JOptionPane.showMessageDialog(null, "Successfully!");
        ntxt.setText("");
        Addbtn.setText("");

    }//GEN-LAST:event_AddbtnActionPerformed

