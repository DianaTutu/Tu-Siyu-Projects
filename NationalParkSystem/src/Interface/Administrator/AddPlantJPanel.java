    public boolean namePatternCorrect() {
        Pattern p = Pattern.compile("^[a-zA-Z]{2,20}$");
        Matcher m = p.matcher(ntxt.getText());
        boolean b = m.matches();
        if (b == false) {
            return false;
        }
        return true;
    }


