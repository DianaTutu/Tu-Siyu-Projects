    public boolean emailPatternCorrect() {
        Pattern p = Pattern.compile("^([a-z0-9A-Z]+[._a-z0-9A-Z]?[a-z0-9A-Z]+)@([a-z0-9A-Z]+[a-z0-9A-Z_]?[a-z0-9A-Z]+\\.)+[a-zA-Z]{2,}$");
        Matcher m = p.matcher(etxt.getText());
        boolean b = m.matches();
        if (b == false) {
            return false;
        }
        return true;
    }
    
