      public void checkButton() {
        if (ntxt.getText().isEmpty()) {
            subBtn.setEnabled(false);
            return;
        }
        if (utxt.getText().isEmpty()) {
            subBtn.setEnabled(false);
            return;
        }
        if (etxt.getText().isEmpty()) {
            subBtn.setEnabled(false);
            return;
        }
       if(ptxt.getText().isEmpty()){
           subBtn.setEnabled(false);
       }
        
        if (!ptxt.getText().equals(cptxt.getText())) {
            subBtn.setEnabled(false);
            return;
        }
        subBtn.setEnabled(true);

    }
     
     
