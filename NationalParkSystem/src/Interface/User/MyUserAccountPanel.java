    public String parser(Date day) {
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy/MM/dd");
        return sdf.format(day);

    }

    private void populateRequestTable() {
        DefaultTableModel model = (DefaultTableModel) tourist.getModel();
        model.setRowCount(0);
        for (ReservationWorkRequest request : account.getWorkQueue().getReservationWorkRequests()) {

            Object[] row = new Object[4];
            row[1] = parser(request.getRequestDate());
            //row[0] = request;
            row[0] = request.getMessage();
            row[2] = request.getStatus();
            model.addRow(row);
        }

    }

