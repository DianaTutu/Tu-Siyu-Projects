    private void okBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okBtnActionPerformed
        // TODO add your handling code here:
        if (commentsTXT.getText().isEmpty()){
            return;
        }
        CommentsWorkRequest cwr=new CommentsWorkRequest();
        cwr.setMessage(commentsTXT.getText());
        cwr.setSender(account);
        cwr.setType("comment");
        // Add the comments to the sender's account:
        account.getWorkQueue().getCommentsWorkRequests().add(cwr);
        // Add the comments to the User Organization:
        enterprise.getOrganizationDirectory().getOrganizationList().get(0).getWorkQueue().getCommentsWorkRequests().add(cwr);
        
        JOptionPane.showMessageDialog(null, "Successfully submitted!");
        commentsTXT.setText("");
    }//GEN-LAST:event_okBtnActionPerformed

