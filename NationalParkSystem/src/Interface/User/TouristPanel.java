    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        /**
         * Gong
         */
        if (date.getDate() == null || numbers.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter all field!");
            return;
        }
        if (!isNumber()) {
            numbers.setBackground(Color.red);
            JOptionPane.showMessageDialog(null, "Wrong number pattern");
            return;
        }
        numbers.setBackground(Color.white);
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        Date de = date.getDate();
        int num = Integer.parseInt(numbers.getText());

        // validation: cannot exceed 200 people for one day
        int people = 0;
        for (ReservationWorkRequest rr : enterprise.getOrganizationDirectory().getOrganizationList().get(0).getWorkQueue().getReservationWorkRequests()) {
            if ((sdf.format(rr.getRequestDate()).equalsIgnoreCase(sdf.format(de)) || sdf.format(rr.getRequestDate()) == sdf.format(de)) && (rr.getStatus() == "Approved" || rr.getStatus().equalsIgnoreCase("approved"))) {
                people = people + Integer.parseInt(rr.getMessage());
            }
        }

        if (people + num > 200) {
            JOptionPane.showMessageDialog(null, "Fully booked! Please select another date!");
            return;
        }
        // validation ends

        ReservationWorkRequest request = new ReservationWorkRequest();

        request.setMessage(String.valueOf(num));
        request.setRequestDate(de);
        request.setSender(userAccount);
        request.setStatus("Under Review");

        enterprise.getOrganizationDirectory().getOrganizationList().get(0).getWorkQueue().getReservationWorkRequests().add(request);

        userAccount.getWorkQueue().getReservationWorkRequests().add(request);
        JOptionPane.showMessageDialog(null, "Request has been sent!");
        numbers.setText("");
        date.setDate(null);


    }//GEN-LAST:event_jButton1ActionPerformed


