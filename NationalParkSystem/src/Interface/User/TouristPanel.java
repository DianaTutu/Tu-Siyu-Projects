    public boolean isNumber() {
        Pattern p = Pattern.compile("^[0-9]*[1-9][0-9]*$");
        Matcher m = p.matcher(numbers.getText());
        boolean b = m.matches();
        if (b == false) {
            return false;
        }
        return true;

    }

