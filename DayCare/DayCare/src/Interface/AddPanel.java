    private void sBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sBtnActionPerformed
        // TODO add your handling code here:
        boolean y = true;
        
        try{
            int x = Integer.parseInt(idText.getText()); 
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "You should enter a number for ID.");
            idText.setText(null); 
            y = false;
        }
        
        if (y) {
        	try {
                Date date = parser(dbText.getText());
            } catch (ParseException e) {
            	JOptionPane.showMessageDialog(null, "You should enter the Birthday in the form of YYYY/MM/DD.");
                dbText.setText(null); 
                y = false;
            }
        }
        
        if (y) {
        	try {
                Date date = parser(rdText.getText());
            } catch (ParseException e) {
            	JOptionPane.showMessageDialog(null, "You should enter the Register Date in the form of YYYY/MM/DD.");
                rdText.setText(null); 
                y = false;
            }
        }
        
        if (y) {
        	try{
                int x = Integer.parseInt(peText.getText()); 
            }catch(NumberFormatException e){
                JOptionPane.showMessageDialog(null, "You should enter a number for period.");
                peText.setText(null); 
                y = false;
            }
        }
        
        if (y) {
        	try{
                int x = Integer.parseInt(ritText.getText()); 
            }catch(NumberFormatException e){
                JOptionPane.showMessageDialog(null, "You should enter a number for Required Inject Times.");
                ritText.setText(null); 
                y = false;
            }
        }
        
        if (y) {
        	try {
                Date date = parser(ideText.getText());
            } catch (ParseException e) {
            	JOptionPane.showMessageDialog(null, "You should enter the inject Date in the form of YYYY/MM/DD.");
                ideText.setText(null); 
                y = false;
            }
        }
        
        if (y) {
        	try{
                int x = Integer.parseInt(itsText.getText()); 
            }catch(NumberFormatException e){
                JOptionPane.showMessageDialog(null, "You should enter a number for Injected Times.");
                itsText.setText(null); 
                y = false;
            }
        }
        
        
        if (y) {
            //Student student = studentDir.addStudent();
            
            Student student = start.getStudentDirectory().addStudent();
            
            student.setFirstname(fnText.getText());
            student.setLastname(lnText.getText());
            student.setId(Integer.parseInt(idText.getText()));
            student.setMotherName(mnText.getText());
            student.setFatherName(dnText.getText());
            student.setBirthday(dbText.getText());
            student.setRegisterDate(rdText.getText());
            now = start.getNow();
            birthDay = dbText.getText();
            try {
                student.setAge(now,birthDay);
            } catch (ParseException ex) {
                Logger.getLogger(AddPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            String immunetype = iteText.getText();
            String period = peText.getText();
            String rit = ritText.getText();
            String ide = ideText.getText();
            String its = itsText.getText();
            
            student.setImuDir(student.readImu(immunetype, period, rit, ide, its));
            
            JOptionPane.showMessageDialog(null, "Student created successful");
            
            fnText.setText(null);   
            lnText.setText(null);
            idText.setText(null);
            mnText.setText(null);
            dnText.setText(null);
            dbText.setText(null);
            rdText.setText(null);
            
        }
        
        
    }//GEN-LAST:event_sBtnActionPerformed

    public Date parser(String birthDay) throws ParseException {
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy/MM/DD");
        return sdf.parse(birthDay);
    }

